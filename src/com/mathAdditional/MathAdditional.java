package com.mathAdditional;

/**
 *  The class {@code MathAdditional} consists of making available widely used
 *  values with ease
 *
 * @author Eduardo Ribeiro Leal
 * @version 1.0.0
 * @see com.mathAdditional
 */
public class MathAdditional {

    /**
     * No instances for you
     */
    private MathAdditional(){}

    /**
     * The {@code double} value that is closer than any other to
     * <i>pi</i>, the ratio of the circumference of a circle to its
     * diameter.
     */
    double PI = 3.14159265358979323846;

    /**
     * The {@code double} value that is closer than any other to
     * <i>e</i>, the base of the natural logarithms.
     */
    double E = 2.7182818284590452354;

    /**
     * Constant by which to multiply an angular value in degrees to obtain an
     * angular value in radians.
     */
    double DEGREES_TO_RADIANS = 0.017453292519943295;

    /**
     * Constant by which to multiply an angular value in radians to obtain an
     * angular value in degrees.
     */
    double RADIANS_TO_DEGREES = 57.29577951308232;
}
